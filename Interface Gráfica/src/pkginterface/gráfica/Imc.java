    private void btnImcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImcActionPerformed
        peso = Float.parseFloat(txtPeso.getText());
        altura = Float.parseFloat(txtAltura.getText());
        
        imc = peso / (altura*altura);
        
        txtResultado.setText(String.valueOf(imc));
        
        if (imc < 18.5) {
            JOptionPane.showMessageDialog(null, "Abaixo do Peso Normal");
        } 
        if (imc >= 18.5 && imc <=24.9) {
            JOptionPane.showMessageDialog(null, "Peso Ideal");
        }
        if (imc >= 25 && imc <=29.9) {
            JOptionPane.showMessageDialog(null, "Sobrepeso");
        }
        if (imc >= 30 && imc <=34.9) {
            JOptionPane.showMessageDialog(null, "Obesidade grau I");
        }
        if (imc >= 35 && imc <=39.9) {
            JOptionPane.showMessageDialog(null, "Obesidade grau II");
        }
        if (imc >= 40) {
            JOptionPane.showMessageDialog(null, "Obesidade classe III");
   
        }
    }//GEN-LAST:event_btnImcActionPerformed

